# -*- coding: utf-8 -*-
import time

def viajar_tiempo():
    print("\n⏳ Activando motores temporales...")
    time.sleep(1)
    print("🌀 Sincronizando coordenadas espacio-temporales...")
    time.sleep(1)
    print("✅ ¡Viaje en el tiempo iniciado! 🚀\n")

if __name__ == "__main__":
    viajar_tiempo()
